"""Build the messages-format SFT dataset for fine-tuning.

Combines raw article text with Claude's reasoning + labels to produce
the `{"messages": [...]}` jsonl that TRL's SFT trainer expects. One line
per example, each with three turns:

    system:     slim_system_instruction (the Table 1 prompt)
    user:       "### Article:\n{title}\n\n{content}"  (truncated to --max-chars)
    assistant:  "<think>\n{reasoning}\n</think>\n\n```yaml\n<labels>\n```"

Refuses to overwrite an existing output file unless --force is passed —
the committed `data/ft_data/sonnet_ft_v2.jsonl` was generated in a notebook
and shouldn't be regenerated by accident.

Usage:
    python classification/build_sft_dataset.py \
        --articles data/annotation_sample_5k.jsonl \
        --annotations data/classifications/annotations_5k.jsonl \
        --output data/ft_data/sonnet_ft_v2.jsonl

Inputs
------
--articles: JSONL with at least `id`, `title`, `content`.
--annotations: JSONL with `id`, `reasoning`, and the four label keys:
    climate_change, health, health_effects_of_climate_change,
    health_effects_of_extreme_weather.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from prompts.base_prompt import slim_system_instruction

LABELS = [
    "climate_change",
    "health",
    "health_effects_of_climate_change",
    "health_effects_of_extreme_weather",
]

SYSTEM_PROMPT = slim_system_instruction.strip()


def _truncate(text: str, max_chars: int) -> str:
    return text if len(text) <= max_chars else text[:max_chars] + "..."


def _user_turn(title: str, content: str, max_chars: int) -> str:
    body = f"{title or ''}\n\n{content or ''}".strip()
    return f"### Article:\n{_truncate(body, max_chars)}"


def _assistant_turn(reasoning: str, ann: dict) -> str:
    yaml_lines = ["```yaml"]
    for label in LABELS:
        yaml_lines.append(f"{label}:\n  label: {'true' if ann[label] else 'false'}")
    yaml_lines.append("```")
    yaml_block = "\n".join(yaml_lines)
    return f"<think>\n{reasoning.strip()}\n</think>\n\n{yaml_block}"


def _load_jsonl(path: Path) -> list[dict]:
    rows = []
    with path.open() as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def build_rows(articles: list[dict], annotations: list[dict], max_chars: int) -> list[dict]:
    ann_by_id = {a["id"]: a for a in annotations if "error" not in a}
    rows = []
    skipped_no_annotation = 0
    skipped_bad_annotation = 0
    for art in articles:
        ann = ann_by_id.get(art["id"])
        if ann is None:
            skipped_no_annotation += 1
            continue
        missing = [k for k in (*LABELS, "reasoning") if k not in ann]
        if missing:
            skipped_bad_annotation += 1
            continue
        rows.append({
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": _user_turn(art.get("title", ""), art.get("content", ""), max_chars)},
                {"role": "assistant", "content": _assistant_turn(ann["reasoning"], ann)},
            ]
        })
    if skipped_no_annotation:
        print(f"  skipped (no annotation):  {skipped_no_annotation}")
    if skipped_bad_annotation:
        print(f"  skipped (missing fields): {skipped_bad_annotation}")
    return rows


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--articles", type=Path, required=True)
    parser.add_argument("--annotations", type=Path, required=True)
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--max-chars", type=int, default=10000)
    parser.add_argument("--force", action="store_true", help="Overwrite --output if it exists")
    args = parser.parse_args()

    if args.output.exists() and not args.force:
        sys.exit(f"refusing to overwrite {args.output}; pass --force to regenerate")

    print(f"Reading articles    from {args.articles}")
    articles = _load_jsonl(args.articles)
    print(f"  {len(articles)} articles")
    print(f"Reading annotations from {args.annotations}")
    annotations = _load_jsonl(args.annotations)
    print(f"  {len(annotations)} annotations")

    rows = build_rows(articles, annotations, args.max_chars)
    print(f"Built {len(rows)} SFT examples")

    args.output.parent.mkdir(parents=True, exist_ok=True)
    with args.output.open("w") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
